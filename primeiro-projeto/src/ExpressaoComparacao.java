public class ExpressaoComparacao {

    public static void main(String[] args) {

        int idade = 23;
        int profissaoTi = 5;

        System.out.println(idade == profissaoTi);
        System.out.println(idade != profissaoTi);
        System.out.println(idade > profissaoTi);
        System.out.println(idade < profissaoTi);
        System.out.println(idade >= profissaoTi);
        System.out.println(idade <= profissaoTi);
    }
}
